public class Code02_02 {
    public static void main(String[] args) {
        // 변수 : 값을 저장하는 메모리 공간
        int a = 100; // 정수형 변수 a에 100을 기입
        int b = 200; // 정수형 변수 b에 200을 기입
        /* 프로그래밍 언어에서 =의 의미는 값을 기입하라는 의미
        * ==의 의미가 같다는 의미
        * a = b;는 a에 b를 기입해라
        * a == b; 는 a와 b는 같다 */
    }
}
