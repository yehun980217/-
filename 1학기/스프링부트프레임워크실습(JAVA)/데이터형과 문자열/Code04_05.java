public class Code04_05 {
    public static void main(String[] args) {
        System.out.printf("200+300");
        System.out.println();
        System.out.printf("%d", 200+300); // %d는 정수를 의미하며 이러한 것을 서식이라한다.
        System.out.println();
        System.out.printf("%d %d", 100, 200); // %d가 여러 개인 경우 짝이 맞아야한다.
    }
}