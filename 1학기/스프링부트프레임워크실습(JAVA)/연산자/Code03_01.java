public class Code03_01 {
    public static void main(String[] args) {
        int n1, n2, res;
        n1 = 5;
        n2 = 3;

        // = 기준으로 오른쪽의 값을 왼쪽에 기입
        res = n1 + n2;
        System.out.println(res);

        res = n1 - n2;
        System.out.println(res);

        res = n1 * n2;
        System.out.println(res);

        res = n1 / n2;
        System.out.println(res);

        res = n1 % n2;
        System.out.println(res);
    }
}
