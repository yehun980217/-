public class Code05_03 {
    public static void main(String[] args) {
        int num = 200;

        if (num < 100) { // num이 100보다 작다면~
            System.out.println("100보다 작습니다."); // num이 100보다 작은 게 참인 경우 해당 코드 수행
        } else { // num이 100보다 작지않다면~
            System.out.println("100보다 큽니다."); // num이 100보다 작지않다면 즉 거짓인 경우 해당 코드 수행
        }
    }
}
