public class Code02_03 {
    public static void main(String[] args) {
        int a = 100; // 정수형 변수 a에 100 기입
        int b = 200; // 정수형 변수 b에 200 기입
        int c = a + b; // a(100)와 b(200)을 더한 값인 300을 c에 기입
        System.out.println("c의 값 ==> " + c); // c의 값을 출력하는 코드
        System.out.println(a + " + " + b + " = " + c); // 전체 계산식이 출력되는 코드
    }
}
